<h5>
    The Nigerian Institute of Leather and Science Technology, is located in Samaru, Zaria, opposite the Main Campus of Ahmadu Bello University, Zaria. The College began as a modest Organization in 1964. Over the years, it has expanded from Hides and Skins Demonstration and Training Project established by the FAO/UNDP in collaboration with the Federal Government and the then Northern Nigeria Government to a fully fledged tertiary Institution. <br>Within this period, the College has had its name changed several times. In 1971, FAO/UNDP handed the project to the Federal Government through the Federal Livestock Department and was renamed Federal Leather Institute. During this period, the principal function of the Institute was the in-service training of Hides and Skins improvement Staff and Leather Technologists. In 1976, the Leather Institute was converted into the Leather Research Institute of Nigeria (LERIN) amongst other Research Institutes.<br><br>

Their activities were coordinated by the Agricultural Research Council of Nigeria (ARCN), a body established by the Federal Military Government. The National Science and Technology Development Agency (NSTDA) were incorporated two years later to cater for all research Institutes, while ARCN became a Division together with its Research Institutes. The NSTDA later became the Federal Ministry of Science and Technology. Under the Research Institute, the College was one of the 14 Research Programmes with the responsibility of producing the much-needed manpower in Hides and Skins and Leather Technology for the Country. There were three courses available leading to the award of:-<br><br>

1. Higher National Diploma in Leather Technology<br>
2. Certificate in Leather Craft<br>
3. Higher Level Certificate in Hides and Skins Improvement <br>
4. Lower Level Certificate in Hides and Skins Improvement<br>

In 1988, the functions and mandate of LERIN were expanded to include Research into all aspects of Chemical technology and it was renamed National Research Institute for Chemical Technology (NARICT). The College then became Federal College of Leather Technology (FCLT) under NARICT with semi-autonomous status. The College achieved full autonomy in November 1991. The name changed in late 1992 to Federal College of Chemical and Leather Technology (CHELTECH). <br><br>
With the change in name, the mandate was expanded to include training in all aspects of Chemical and Leather Technologies as well as training for the award of HND and ND and certificate programmes, in different disciplines. The name changed in late 2011 to Nigerian Instutite of Leather and Science Technology (NILEST). With the change in name, the mandate was expanded to include training in all aspects of Science and Leather Technologies as well as training for the award of HND and ND and certificate programmes, in different disciplines.</h5>
